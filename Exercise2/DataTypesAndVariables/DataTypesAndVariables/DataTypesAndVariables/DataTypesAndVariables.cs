using System;

namespace DataTypesAndVariables
{
    class DataTypesAndVariables
    {
        static void Main(string[] args)
        {
            sbyte a = -100;
            byte b = 128;
            short c = -3540;
            uint d = 64876;
            ulong e = 2147483648;
            long f = -1141583228;
            long j = -1223372036854775808;

            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
            Console.WriteLine(j);
        }
    }
}
